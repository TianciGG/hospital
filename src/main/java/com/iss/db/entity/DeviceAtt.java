package com.iss.db.entity;

public class DeviceAtt {
	private String equipId;

	private String factory;

	private String deviceName;

	private String dnumber;

	private String buyDate;

	private String checkCircle;

	private String attid;

	private String attname;

	private String atturl;

	private String keshiCode;

	private String supportCode;

	public String getEquipId() {
		return equipId;
	}

	public void setEquipId(String equipId) {
		this.equipId = equipId;
	}

	public String getFactory() {
		return factory;
	}

	public void setFactory(String factory) {
		this.factory = factory;
	}

	public String getDeviceName() {
		return deviceName;
	}

	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}

	public String getDnumber() {
		return dnumber;
	}

	public void setDnumber(String dnumber) {
		this.dnumber = dnumber;
	}

	public String getBuyDate() {
		return buyDate;
	}

	public void setBuyDate(String buyDate) {
		this.buyDate = buyDate;
	}

	public String getCheckCircle() {
		return checkCircle;
	}

	public void setCheckCircle(String checkCircle) {
		this.checkCircle = checkCircle;
	}

	public String getAttid() {
		return attid;
	}

	public void setAttid(String attid) {
		this.attid = attid;
	}

	public String getAttname() {
		return attname;
	}

	public void setAttname(String attname) {
		this.attname = attname;
	}

	public String getAtturl() {
		return atturl;
	}

	public void setAtturl(String atturl) {
		this.atturl = atturl;
	}

	public String getKeshiCode() {
		return keshiCode;
	}

	public void setKeshiCode(String keshiCode) {
		this.keshiCode = keshiCode;
	}

	public String getSupportCode() {
		return supportCode;
	}

	public void setSupportCode(String supportCode) {
		this.supportCode = supportCode;
	}

}
